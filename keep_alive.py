from flask import Flask
import threading

app = Flask('')

# ==============================================================================
# -*-*- ASOSIY SAHIFA -*-*-
# ==============================================================================
@app.route('/')
def home():
    return "Bot is alive!"

# ==============================================================================
# -*-*- SERVERNI ISHGA TUSHIRISH -*-*-
# ==============================================================================
def run():
    app.run(host='0.0.0.0', port=8080)

# ==============================================================================
# -*-*- DOIMIY ISHLASH FUNKSIYASI -*-*-
# ==============================================================================
def keep_alive():
    server = threading.Thread(target=run)
    server.start()